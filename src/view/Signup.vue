<script setup>
  import { supabase } from "../supabase"
  import { useRouter } from "vue-router"
  import { ref } from "vue"

  let errorMsg = ref(null)
  let email = ref("")
  let password = ref("")
  let confirmPassword = ref("")
  const router = useRouter()

  const handleRegister = async () => {
    if (password.value === confirmPassword.value) {
      try {
        const { error } = await supabase.auth.signUp({
          email: email.value,
          password: password.value,
        })
        if (error) throw error
        router.push({ name: "EmailConfirmation" })
      } catch (error) {
        errorMsg.value = error.message
      }
      return
    } else {
      errorMsg.value = "Error: Password do not match"
    }
  }
</script>

<template>
  <div class="height-wrapper padding-wrapper">
    <div class="max-w-2xl px-4 mx-auto">
      <!-- Error handling -->
      <div v-if="errorMsg" class="relative p-4 mb-10 rounded-md shadow-lg bg-light-grey">
        <p class="text-red-500">{{ errorMsg }}</p>
        <button class="absolute top-0 text-lg right-2" @click="errorMsg = null">
          &times;
        </button>
      </div>
      <div class="p-8 rounded-md shadow-md bg-[#ffffff]">
        <h1 class="mb-4 text-xl font-semibold text-info">Sign up</h1>
        <form class="flex flex-col gap-y-6" @submit.prevent="handleRegister">
          <div class="flex flex-col">
            <label class="mb-1" for="email">Email*</label>
            <input
              id="email"
              v-model="email"
              class="px-2.5 py-3.5 focus:outline-none"
              type="email"
            />
          </div>
          <div class="flex flex-col">
            <label class="mb-1" for="password">Password*</label>
            <input
              id="password"
              v-model="password"
              class="px-2.5 py-3.5 focus:outline-none"
              type="password"
            />
          </div>
          <div class="flex flex-col">
            <label class="mb-1" for="confirm password">Confirm password*</label>
            <input
              id="confirm password"
              v-model="confirmPassword"
              class="px-2.5 py-3.5 focus:outline-none"
              type="password"
            />
          </div>

          <button
            class="px-10 py-3.5 font-medium tracking-wider text-gray-100 uppercase sm:self-start bg-info"
            type="submit"
          >
            Register
          </button>
          <p class="text-right text-gray-600">
            Already have an account?
            <router-link :to="{ name: 'Login' }" class="text-info">Login</router-link>
          </p>
        </form>
      </div>
    </div>
  </div>
</template>

